if Inventory.pets then
    for PetUid, PetData in pairs(Inventory.pets) do
        local petCategory = InventoryDB[PetData.category]
        if petCategory and petCategory[PetData.id] then
            local petInfo = petCategory[PetData.id]
            local petName = petInfo["name"]
            local petFound = false
            for _, petValue in pairs(PetValues) do
                if string.lower(petValue.name) == string.lower(petName) then
                    petFound = true
                    local valueToUse
                    if PetData.properties.mega_neon then
                        valueToUse = tonumber(petValue.megaValue)
                    elseif PetData.properties.neon then
                        valueToUse = tonumber(petValue.neonValue)
                    else
                        valueToUse = tonumber(petValue.regularValue)
                    end
                    totalValue = totalValue + (valueToUse or 0)
                    break
                end
            end
            if not petFound then
                warn(petName)
            end
        else
            warn("no category")
        end
    end
    return totalValue
